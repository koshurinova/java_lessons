public class If {
    public static void main(String [] args) {
        int myInt=15;
        if (myInt<10){
            System.out.println("Да, меньше 10");
        }else if(myInt>20){
            System.out.println("Да, больше 20");
        }else{
            System.out.println("Ни один из предыдущих случаев");

        }
    }

}
